// Calculate the CRC polynomial '100100101'
uint8_t CRC8(const void *pData, uint32_t DataLength)
{
        uint8_t CRC8 = 0xFF;

        while (DataLength--)
        {
                CRC8=SYS_CRC8Table[CRC8^*(const uint8_t *)pData];
                pData=(const void *)((const uint8_t *)pData+1);
        }
        return CRC8;
}
